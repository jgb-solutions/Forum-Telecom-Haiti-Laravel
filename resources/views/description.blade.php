                <h2>Description de l&#39;activité</h2>

                <p>Les 17 et 18 mai 2011 s&#39;est tenue au Karibe convention Center, la conférence HAITI TELECOM

EXPO qui deviendra cinq ans plus tard, soit en 2016, « Forum Telecom Haïti », une initiative

de GESTE-HAITI. Plusieurs Intervenants et partenaires tels que le groupe Croissance avec

Kesner Pharel, Technologie et Communication Solution S.A avec Pierre Samuel et Braincure

Company pour ne citer que ceux-là, ont participé à ce grand événement qui a attiré des

centaines de personnes de différents secteurs de la société.</p>

                <p>Toutefois, nous nous sommes rendu compte que tout le monde ne profitait pas des retombées

socio-économiques des télécommunications. Cette année encore GESTE-Haïti organise «

Forum Telecom Haïti », un forum dans le cadre de la journée mondiale des

télécommunications et de la société de l’information (17 Mai) afin de réunir les différents acteurs

et spécialistes du secteur des télécommunications, pour faire un panorama des questions liées

au développement des réseaux et des services de l’écosystème numérique, et sur l’impact

social des NTIC en Haïti.</p>
